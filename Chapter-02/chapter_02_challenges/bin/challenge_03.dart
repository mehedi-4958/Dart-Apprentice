void challenge3() {

  // sets a constant integer variable  x
  const x = 46;

  // sets a constant integer variable y
  const y = 10;

  // assigns the value from the calculation to the constant integer variable answer1
  const answer1 = (x * 100) + y; //answer1 = 4610
  // prints answer1
  print("Challenge-3: answer1 = $answer1");

  // assigns the value from the calculation to the constant integer variable answer2
  const answer2 = (x * 100) + (y * 100); //answer2 = 5600
  // prints answer2
  print("Challenge-3: answer2 = $answer2");

  // assigns the value from the calculation to the constant double variable answer1
  const answer3 = (x * 100) + (y / 10); //answer3 = 4601.0
  // prints answer3
  print("Challenge-3: answer3 = $answer3");
}